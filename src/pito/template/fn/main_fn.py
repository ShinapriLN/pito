from datetime import datetime

def getSomething():
    return "This is something!"

def getSomethingButWithArgs(value):
    return f"You sent me this: {value}"

def getTitle():
    return "Sample Document Generated by Pito"

def getAuthor():
    return "Anonymous User"

def getDate():
    return datetime.now().strftime("%Y-%m-%d")

def getIntroText():
    return (
        "This document demonstrates the integration of LaTeX syntax and Pito dynamic functions.\n"
        "You can combine text with mathematical expressions seamlessly using double dollar blocks."
    )

def generateTable(path):
    return f"\\textbf{{[Table generated from {path}]}}"

def getConclusion():
    return "This section summarizes the main points and findings of the document."

def getFooter(msg):
    return f"\\vspace{{1em}}\\begin{{center}}\\textit{{{msg}}}\\end{{center}}"
